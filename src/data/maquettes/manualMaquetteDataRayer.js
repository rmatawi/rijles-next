const convertFullMaquetteToMini = (full) => {
    const result = { ...full };
    const sides = ["top", "left", "right", "bottom"];

    for (let sideIndex = 0; sideIndex < sides.length; sideIndex += 1) {
        const side = sides[sideIndex];
        const { vehicles: sourceRows = [], ...sideMeta } = full[side] || {};
        const vehicles = new Array(sourceRows.length);

        for (let rowIndex = 0; rowIndex < sourceRows.length; rowIndex += 1) {
            const sourceRow = sourceRows[rowIndex] || [];
            const row = new Array(sourceRow.length);

            for (let colIndex = 0; colIndex < sourceRow.length; colIndex += 1) {
                const cell = sourceRow[colIndex] || {};

                row[colIndex] = cell.type === "space" ? {} : cell;
            }

            vehicles[rowIndex] = row;
        }

        result[side] = { ...sideMeta, vehicles };
    }

    return result;
};

